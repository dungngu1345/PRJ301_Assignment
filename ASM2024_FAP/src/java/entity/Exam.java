package entity;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Exam {
    private int id;
    private int aid;
    private Date begin;
    private Date end;

    public Exam() {
    }

    public Exam(int id, int aid, Date begin, Date end) {
        this.id = id;
        this.aid = aid;
        this.begin = begin;
        this.end = end;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }

    public Date getBegin() {
        return begin;
    }

    public void setBegin(Date begin) {
        this.begin = begin;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
    
}
