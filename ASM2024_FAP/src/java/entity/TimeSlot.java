package entity;

/**
 *
 * @author Admin
 */
public class TimeSlot {
    private int id;
    private String note;

    public TimeSlot() {
    }

    public TimeSlot(int id, String note) {
        this.id = id;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    
}
